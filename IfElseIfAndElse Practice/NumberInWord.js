// 1. Read single digit number and write the number in word
const prompt = require('prompt-sync')();
const inputNumber = prompt('Enter a single digit number : ');
if (inputNumber == 1) 
{
    console.log("One");
}
else if (inputNumber == 2)
{
    console.log("Two");
}
else if (inputNumber == 3) 
{
    console.log("Three");
}
else if (inputNumber == 4) 
{
    console.log("Four");
}
else if (inputNumber == 5) 
{
    console.log("Five");
}
else if (inputNumber == 6) 
{
    console.log("Six");
}
else if (inputNumber == 7) 
{
    console.log("Seven");
}
else if (inputNumber == 8) 
{
    console.log("Eight");
}
else if (inputNumber == 9) 
{
    console.log("Nine");
}
else 
{
    console.log("Invalid Number");
}